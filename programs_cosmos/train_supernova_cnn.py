def main():
    parser = argparse.ArgumentParser(
        description="Train CNN to detect supernovae."
    )
    parser.add_argument("--hst-dir",
        default="/data/astrofs2_1/suzuki/data/HST/cosmosacs/original_png/")
    parser.add_argument("--jwst-dir",
        default="/data/astrofs2_1/suzuki/data/JWST/cosmosweb/v0.8_png")
    parser.add_argument("--epochs", type=int, default=50)
    parser.add_argument("--batch-size", type=int, default=64)
    parser.add_argument("--lr", type=float, default=1e-3)
    parser.add_argument("--num-artificial", type=int, default=5000)
    parser.add_argument("--num-workers", type=int, default=4)
    parser.add_argument("--inference-workers", type=int, default=8)
    parser.add_argument("--inference-batch-size", type=int, default=256)
    parser.add_argument("--model-path", default="supernova_cnn.pth")
    parser.add_argument("--output-csv", default="supernova_cnn_candidates.csv")
    parser.add_argument(
        "--known-sn-csv", default=DEFAULT_KNOWN_SN_CSV,
        help=f"CSV of confirmed supernovae (default: {DEFAULT_KNOWN_SN_CSV})"
    )
    parser.add_argument(
        "--skip-train", action="store_true",
        help="Skip training; load existing weights from --model-path and "
             "go straight to inference"
    )
    args = parser.parse_args()

    # Load ground-truth catalog and derive id sets
    known_sn = load_known_supernovae(args.known_sn_csv)
    known_sn_set = set(known_sn.keys())
    verified_sn_ids = {
        oid for oid in known_sn_set
        if oid.isdigit() and int(oid) <= VERIFIED_MAX_ID
    }
    print(f"Loaded {len(known_sn)} confirmed supernovae from {args.known_sn_csv}")
    print(f"  In verified range (ID <= {VERIFIED_MAX_ID}): {len(verified_sn_ids)}")

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"Device: {device}")

    # ==================================================================
    # STEP 1: Load and match files
    # ==================================================================
    print("\n" + "="*60)
    print("STEP 1: Loading and matching image files")
    print("="*60)

    file_map = build_file_maps(args.hst_dir, args.jwst_dir)
    paired = {k: v for k, v in file_map.items()
              if "hst" in v and "jwst1" in v and "jwst2" in v}
    print(f"Objects with all 3 images: {len(paired)}")

    known_sn_objects = {k: v for k, v in paired.items() if k in known_sn_set}
    print(f"Known SN found: {len(known_sn_objects)}/{len(known_sn_set)}")
    missing = known_sn_set - set(known_sn_objects.keys())
    if missing:
        print(f"  Missing: {sorted(missing)}")

    # ==================================================================
    # STEP 2: Build training data from verified ground truth
    # ==================================================================
    print("\n" + "="*60)
    print("STEP 2: Building training data")
    print("="*60)

    # Verified positives and negatives from ID range 1-53131
    verified_pos_files = []
    verified_neg_files = []
    for obj_id, files in paired.items():
        try:
            numeric_id = int(obj_id)
        except ValueError:
            continue
        if numeric_id <= VERIFIED_MAX_ID:
            if obj_id in verified_sn_ids:
                verified_pos_files.append(files)
            else:
                verified_neg_files.append(files)

    print(f"Verified range (ID 1-{VERIFIED_MAX_ID}):")
    print(f"  Real SN: {len(verified_pos_files)}")
    print(f"  Confirmed not SN: {len(verified_neg_files)}")

    # Add all known SN (across the full dataset) as additional positives
    for obj_id, files in known_sn_objects.items():
        if obj_id not in verified_sn_ids:
            verified_pos_files.append(files)
    print(f"  + {len(known_sn_objects) - len(verified_sn_ids)} additional known SN")
    print(f"  Total positives: {len(verified_pos_files)}")

    # Measure real SN profiles to drive realistic artificial SN injection
    print("\n--- Measuring real SN profiles for artificial SN injection ---")
    profiles = measure_real_sn_profiles(known_sn, paired, size=IMAGE_SIZE)
    n_jwst_cat = sum(1 for v in known_sn.values() if v == "JWST")
    n_hst_cat = sum(1 for v in known_sn.values() if v == "HST")
    print(f"  JWST profiles measured: {len(profiles['JWST'])}/{n_jwst_cat}")
    print(f"  HST  profiles measured: {len(profiles['HST'])}/{n_hst_cat}")
    if profiles["JWST"]:
        amps = [p["amp1"] for p in profiles["JWST"]]
        sigs = [p["sigma1"] for p in profiles["JWST"]]
        print(f"  JWST amp1: median={np.median(amps):.3f} "
              f"range=[{min(amps):.3f}, {max(amps):.3f}]")
        print(f"  JWST sigma1: median={np.median(sigs):.2f} "
              f"range=[{min(sigs):.2f}, {max(sigs):.2f}]")
    if profiles["HST"]:
        amps = [p["amp"] for p in profiles["HST"]]
        sigs = [p["sigma"] for p in profiles["HST"]]
        print(f"  HST  amp: median={np.median(amps):.3f} "
              f"range=[{min(amps):.3f}, {max(amps):.3f}]")
        print(f"  HST  sigma: median={np.median(sigs):.2f} "
              f"range=[{min(sigs):.2f}, {max(sigs):.2f}]")
    profiles_csv = args.output_csv.replace(".csv", "_profiles.csv")
    save_profiles_csv(profiles, profiles_csv)
    print(f"  Profiles saved to {profiles_csv}")

    # Create datasets
    verified_dataset = VerifiedDataset(
        verified_pos_files, verified_neg_files,
        oversample_pos=500, augment=True)

    # Artificial SN for additional variety, sampled from real SN profile distribution
    art_dataset = ArtificialSNDataset(
        verified_neg_files[:5000],
        num_samples=args.num_artificial, augment=True,
        profiles=profiles)
    print(f"Artificial SN samples: {args.num_artificial} "
          f"(profile-driven: {bool(profiles['JWST'] or profiles['HST'])})")

    train_dataset = torch.utils.data.ConcatDataset([verified_dataset, art_dataset])
    train_loader = DataLoader(
        train_dataset, batch_size=args.batch_size, shuffle=True,
        num_workers=args.num_workers, pin_memory=True)
    print(f"Total training samples: {len(train_dataset)}")

    # ==================================================================
    # STEP 3: Train CNN (skipped if --skip-train)
    # ==================================================================
    print("\n" + "="*60)
    print("STEP 3: Training CNN" + (" (SKIPPED)" if args.skip_train else ""))
    print("="*60)

    model = SupernovaCNN().to(device)

    # Prepare known SN files for validation
    known_files_list = [known_sn_objects[oid] for oid in sorted(known_sn_objects.keys())]
    known_ids_list = sorted(known_sn_objects.keys())

    if args.skip_train:
        if not os.path.exists(args.model_path):
            sys.exit(f"--skip-train but model not found at {args.model_path}")
        print(f"Loading existing weights from {args.model_path}")
        model.load_state_dict(
            torch.load(args.model_path, weights_only=True, map_location=device))
    else:
        criterion = nn.BCEWithLogitsLoss()
        optimizer = optim.Adam(model.parameters(), lr=args.lr)
        scheduler = optim.lr_scheduler.ReduceLROnPlateau(
            optimizer, patience=5, factor=0.5)

        best_recovery = 0
        best_epoch = 0

        for epoch in range(1, args.epochs + 1):
            train_loss, train_acc = train_epoch(
                model, train_loader, criterion, optimizer, device)

            # Check known SN recovery every 5 epochs
            if epoch % 5 == 0 or epoch == args.epochs or epoch == 1:
                probs = predict_batch(model, known_files_list, device)
                recovered = sum(1 for p in probs if p >= 0.5)

                # Show each known SN probability
                if epoch % 10 == 0 or epoch == args.epochs:
                    for oid, p in zip(known_ids_list, probs):
                        tag = "OK" if p >= 0.5 else "MISS"
                        print(f"    {oid}: {p:.4f} [{tag}]")

                print(f"Epoch {epoch:3d}/{args.epochs} | "
                      f"loss: {train_loss:.4f} acc: {train_acc:.3f} | "
                      f"Known SN: {recovered}/{len(known_ids_list)}")

                if recovered >= best_recovery:
                    best_recovery = recovered
                    best_epoch = epoch
                    torch.save(model.state_dict(), args.model_path)
                scheduler.step(train_loss)
            else:
                print(f"Epoch {epoch:3d}/{args.epochs} | "
                      f"loss: {train_loss:.4f} acc: {train_acc:.3f}")

        print(f"\nBest: {best_recovery}/{len(known_ids_list)} at epoch {best_epoch}")

    # ==================================================================
    # STEP 4: Validate — set threshold to recover all known SN
    # ==================================================================
    print("\n" + "="*60)
    print("STEP 4: Validating")
    print("="*60)

    model.load_state_dict(torch.load(args.model_path, weights_only=True))
    probs = predict_batch(model, known_files_list, device)

    print(f"\nKnown SN probabilities:")
    for oid, p in sorted(zip(known_ids_list, probs), key=lambda x: x[1], reverse=True):
        print(f"  {oid}: {p:.4f}")

    min_prob = min(probs)
    threshold = max(min_prob * 0.9, 0.01)
    print(f"\nLowest known SN prob: {min_prob:.4f}")
    print(f"Threshold: {threshold:.4f}")

    recovered = sum(1 for p in probs if p >= threshold)
    print(f"Recovery: {recovered}/{len(known_ids_list)}")

    # ==================================================================
    # STEP 5: Apply to all objects
    # ==================================================================
    print("\n" + "="*60)
    print(f"STEP 5: Applying to all {len(paired)} objects")
    print("="*60)

    all_probs_csv = args.output_csv.replace(".csv", "_all_probs.csv")

    class InferenceDataset(Dataset):
        def __init__(self, obj_ids, file_map):
            self.obj_ids = obj_ids
            self.file_map = file_map
        def __len__(self):
            return len(self.obj_ids)
        def __getitem__(self, idx):
            obj_id = self.obj_ids[idx]
            triplet = load_triplet(self.file_map[obj_id])
            if triplet is None:
                return obj_id, torch.zeros(NUM_CHANNELS, IMAGE_SIZE, IMAGE_SIZE), False
            return obj_id, torch.tensor(triplet, dtype=torch.float32), True

    def collate_fn(batch):
        ids = [b[0] for b in batch]
        tensors = torch.stack([b[1] for b in batch])
        valid = [b[2] for b in batch]
        return ids, tensors, valid

    obj_ids_sorted = sorted(paired.keys())
    inf_loader = DataLoader(
        InferenceDataset(obj_ids_sorted, paired),
        batch_size=args.inference_batch_size, shuffle=False,
        num_workers=args.inference_workers, pin_memory=True,
        collate_fn=collate_fn)

    start_time = time.time()
    num_candidates = 0
    completed = 0
    total = len(paired)

    csv_f = open(args.output_csv, "w", newline="")
    csv_w = csv.writer(csv_f)
    csv_w.writerow(["object_id", "probability", "known_sn"])

    all_f = open(all_probs_csv, "w", newline="")
    all_w = csv.writer(all_f)
    all_w.writerow(["object_id", "probability", "known_sn"])

    model.eval()
    try:
        with torch.no_grad():
            for batch_ids, batch_tensors, batch_valid in inf_loader:
                outputs = torch.sigmoid(
                    model(batch_tensors.to(device))).cpu().numpy()

                for i, obj_id in enumerate(batch_ids):
                    prob = float(outputs[i]) if batch_valid[i] else 0.0
                    is_known = "YES" if obj_id in known_sn_set else "NO"
                    all_w.writerow([obj_id, f"{prob:.6f}", is_known])

                    if prob >= threshold:
                        csv_w.writerow([obj_id, f"{prob:.4f}", is_known])
                        csv_f.flush()
                        num_candidates += 1
                        tag = "KNOWN" if is_known == "YES" else "NEW"
                        print(f"  *** SN CANDIDATE: {obj_id} "
                              f"(prob={prob:.4f}) [{tag}] ***")

                completed += len(batch_ids)
                if completed % 5000 < args.inference_batch_size or completed == total:
                    elapsed = time.time() - start_time
                    rate = completed / elapsed if elapsed > 0 else 0
                    eta = (total - completed) / rate if rate > 0 else 0
                    all_f.flush()
                    print(f"  Progress: {completed}/{total} "
                          f"({completed/total*100:.1f}%) "
                          f"| {rate:.0f} obj/s | ETA: {eta:.0f}s "
                          f"| Candidates: {num_candidates}")
    finally:
        csv_f.close()
        all_f.close()

    elapsed = time.time() - start_time
    print(f"\nDone in {elapsed:.1f}s")

    # ==================================================================
    # STEP 6: Final check
    # ==================================================================
    print("\n" + "="*60)
    print("STEP 6: Final verification")
    print("="*60)

    candidate_ids = set()
    with open(args.output_csv) as f:
        for row in csv.DictReader(f):
            candidate_ids.add(row["object_id"])

    known_in = set(known_sn_objects.keys())
    found = known_in & candidate_ids
    missed = known_in - candidate_ids
    rate = len(candidate_ids) / total * 1000 if total > 0 else 0

    print(f"Known SN recovered: {len(found)}/{len(known_in)}")
    for oid in sorted(found):
        print(f"  FOUND: {oid}")
    for oid in sorted(missed):
        print(f"  MISSED: {oid}")
    print(f"\nTotal candidates: {len(candidate_ids)}")
    print(f"Detection rate: {rate:.2f} per 1000")
    print(f"Threshold: {threshold:.4f}")
    print(f"\nFiles: {args.output_csv}, {all_probs_csv}")


if __name__ == "__main__":
    main()

# Training
# ============================================================

def train_epoch(model, loader, criterion, optimizer, device):
    model.train()
    total_loss = 0
    correct = 0
    total = 0
    for images, labels in loader:
        images, labels = images.to(device), labels.to(device)
        optimizer.zero_grad()
        outputs = model(images)
        loss = criterion(outputs, labels)
        loss.backward()
        optimizer.step()
        total_loss += loss.item() * len(labels)
        preds = (torch.sigmoid(outputs) > 0.5).float()
        correct += (preds == labels).sum().item()
        total += len(labels)
    return total_loss / total, correct / total


def predict_batch(model, files_list, device, batch_size=256):
    """Predict probabilities for a list of file dicts."""
    model.eval()
    probs = []
    with torch.no_grad():
        for i in range(0, len(files_list), batch_size):
            batch_files = files_list[i:i+batch_size]
            tensors = []
            valid = []
            for files in batch_files:
                triplet = load_triplet(files)
                if triplet is not None:
                    tensors.append(torch.tensor(triplet, dtype=torch.float32))
                    valid.append(True)
                else:
                    tensors.append(torch.zeros(NUM_CHANNELS, IMAGE_SIZE, IMAGE_SIZE))
                    valid.append(False)
            batch = torch.stack(tensors).to(device)
            outputs = torch.sigmoid(model(batch)).cpu().numpy()
            for j, v in enumerate(valid):
                probs.append(float(outputs[j]) if v else 0.0)
    return probs



def augment_triplet(triplet):
    """Random flips and 90-degree rotation."""
    if random.random() > 0.5:
        triplet = np.flip(triplet, axis=2).copy()
    if random.random() > 0.5:
        triplet = np.flip(triplet, axis=1).copy()
    k = random.randint(0, 3)
    triplet = np.rot90(triplet, k, axes=(1, 2)).copy()
    return triplet


class VerifiedDataset(Dataset):
    """Dataset from verified ground truth (IDs 1-53131).

    7 real SN as positives, ~53K confirmed negatives.
    Positives are oversampled to appear more frequently.
    """

    def __init__(self, positive_files, negative_files,
                 oversample_pos=500, augment=True):
        # Each positive appears oversample_pos times
        self.positives = positive_files * oversample_pos
        self.negatives = negative_files
        self.all_samples = (
            [(f, 1.0) for f in self.positives] +
            [(f, 0.0) for f in self.negatives]
        )
        self.augment = augment
        print(f"  VerifiedDataset: {len(self.positives)} pos + "
              f"{len(self.negatives)} neg = {len(self.all_samples)} total")

    def __len__(self):
        return len(self.all_samples)

    def __getitem__(self, idx):
        files, label = self.all_samples[idx]
        triplet = load_triplet(files)
        if triplet is None:
            return torch.zeros(NUM_CHANNELS, IMAGE_SIZE, IMAGE_SIZE), torch.tensor(0.0)
        if self.augment:
            triplet = augment_triplet(triplet)
        return torch.tensor(triplet, dtype=torch.float32), torch.tensor(label, dtype=torch.float32)


class ArtificialSNDataset(Dataset):
    """Generate artificial SN by injecting into non-SN objects.

    If `profiles` (from measure_real_sn_profiles) is provided, the injected
    SN are sampled from the empirical real-SN amp/sigma distribution.
    """

    def __init__(self, non_sn_files, num_samples=5000, augment=True,
                 profiles=None):
        self.non_sn_files = non_sn_files
        self.num_samples = num_samples
        self.augment = augment
        self.profiles = profiles

    def __len__(self):
        return self.num_samples

    def __getitem__(self, idx):
        files = random.choice(self.non_sn_files)
        triplet = load_triplet(files)
        if triplet is None:
            return torch.zeros(NUM_CHANNELS, IMAGE_SIZE, IMAGE_SIZE), torch.tensor(0.0)
        triplet = inject_artificial_sn(triplet, profiles=self.profiles)
        if self.augment:
            triplet = augment_triplet(triplet)
        return torch.tensor(triplet, dtype=torch.float32), torch.tensor(1.0, dtype=torch.float32)


